const reciepientAmount = '1';

const senderPrivateKey = '';

const web3 = new Web3('https://mosaicdao.org/aux/1405');

class TransferUT {

  constructor() {}

  async perform() {

    const senderAccount = web3.eth.accounts.privateKeyToAccount(senderPrivateKey);
    console.log('senderAccount ', senderAccount.address);
    web3.eth.accounts.wallet.add(senderAccount);

    const erc20Contract = new web3.eth.Contract(
      erc20Abi,
      erc20Address
    );

    const senderBalance = await erc20Contract.methods.balanceOf(senderAccount.address).call();
    console.log('senderBalance', senderBalance);

    const reciepientBalance = await erc20Contract.methods.balanceOf(reciepientAddress).call();
    console.log('reciepientBalance', reciepientBalance);

    const response = await erc20Contract.methods.transfer(reciepientAddress, reciepientAmount).send({
      from: senderAccount.address,
      gasPrice: '0x3B9ACA00',
      gas: '7050328'
    });
    console.log('response', response);

    const reciepientBalanceNew = await erc20Contract.methods.balanceOf(reciepientAddress).call();
    console.log('reciepientBalanceNew', reciepientBalanceNew);

  }

}

new TransferUT().perform().then(console.log);